# ============================ #
# 4. Forest plot function      #
# ============================ #

# This function creates a forest plot comparing:
# - the Moli-sani estimate
# - the corresponding European reference estimate
#
# When add_orci_labels = TRUE, a right-side column is added showing the
# OR (95% CI) values for each disease.
make_forest_plot <- function(df_scenario,
                             eur_tbl,
                             right_margin = 10,
                             left_margin = 15,
                             hide_090_label = FALSE,
                             add_orci_labels = FALSE,
                             label_size = 3.0,
                             label_y_offset = 0.22,
                             text_col_mult = 1.28) {
  
  # Join scenario-specific results with the European reference table
  df1 <- df_scenario %>%
    left_join(eur_tbl, by = c("outcome" = "trait")) %>%
    mutate(prs_trait1 = factor(prs_trait1, levels = y_levels_fig, ordered = TRUE))
  
  # Create a long-format table with one row per source and disease
  df_plot <- bind_rows(
    df1 %>%
      transmute(
        prs_trait1,
        source = "Moli-sani",
        OR = OR,
        CI_lower = CI_lower,
        CI_upper = CI_upper
      ),
    df1 %>%
      transmute(
        prs_trait1,
        source = "European reference",
        OR = OR_eur,
        CI_lower = CI_lower_eur,
        CI_upper = CI_upper_eur
      )
  ) %>%
    mutate(
      source = factor(source, levels = c("European reference", "Moli-sani"))
    ) %>%
    filter(!is.na(prs_trait1))
  
  # Numeric y positions are used to place the two rows for each disease
  df_plot <- df_plot %>%
    mutate(
      y_num = as.numeric(prs_trait1)
    )
  
  # Optional text labels for OR (95% CI)
  if (add_orci_labels) {
    df_plot <- df_plot %>%
      mutate(
        label = sprintf("%.2f (%.2f\u2013%.2f)", OR, CI_lower, CI_upper),
        y_lab = ifelse(
          source == "Moli-sani",
          y_num - label_y_offset,
          y_num + label_y_offset
        )
      )
  }
  
  # Define x-axis limits on the log scale
  min_CI <- min(df_plot$CI_lower, na.rm = TRUE)
  max_CI <- max(df_plot$CI_upper, na.rm = TRUE)
  
  x_min <- max(0.25, min_CI * 0.95)
  x_max_base <- max_CI * 1.05
  
  x_max <- x_max_base
  x_text <- x_max_base * text_col_mult
  
  if (add_orci_labels) {
    x_max <- x_text * 1.08
  }
  
  # X-axis breaks
  brks <- c(0.25, 0.5, 0.9, 1, 1.5, 2, 2.5)
  
  lab_fun <- function(x) {
    if (hide_090_label) {
      ifelse(abs(x - 0.9) < 1e-9, "", sprintf("%.2f", x))
    } else {
      sprintf("%.2f", x)
    }
  }
  
  # Build plot
  p <- ggplot(df_plot, aes(x = OR, y = y_num, colour = source, shape = source)) +
    geom_vline(xintercept = 1, linetype = "dashed", linewidth = 0.4) +
    
    # European reference estimate shown below the center line
    geom_errorbarh(
      data = df_plot %>% filter(source == "European reference"),
      aes(xmin = CI_lower, xmax = CI_upper),
      height = 0.18,
      linewidth = 0.4,
      position = position_nudge(y = +0.15)
    ) +
    geom_point(
      data = df_plot %>% filter(source == "European reference"),
      size = 2,
      stroke = 0.3,
      position = position_nudge(y = +0.15)
    ) +
    
    # Moli-sani estimate shown above the center line
    geom_errorbarh(
      data = df_plot %>% filter(source == "Moli-sani"),
      aes(xmin = CI_lower, xmax = CI_upper),
      height = 0.18,
      linewidth = 0.4,
      position = position_nudge(y = -0.15)
    ) +
    geom_point(
      data = df_plot %>% filter(source == "Moli-sani"),
      size = 2,
      stroke = 0.3,
      position = position_nudge(y = -0.15)
    ) +
    
    scale_color_manual(values = c(
      "European reference" = "#999999",
      "Moli-sani" = "black"
    )) +
    scale_shape_manual(values = c(
      "European reference" = 15,
      "Moli-sani" = 15
    )) +
    
    scale_x_log10(
      breaks = brks,
      labels = lab_fun,
      minor_breaks = NULL,
      name = "Odds ratio (95% CI)"
    ) +
    coord_cartesian(xlim = c(x_min, x_max), clip = "off") +
    scale_y_reverse(
      breaks = seq_along(y_levels_fig),
      labels = y_levels_fig
    ) +
    labs(y = "Disease", colour = "", shape = "") +
    theme_classic(base_family = "Arial") +
    theme(
      panel.grid = element_blank(),
      axis.line = element_line(linewidth = 0.4),
      axis.ticks = element_line(linewidth = 0.3),
      axis.ticks.length = unit(2, "pt"),
      axis.text.x = element_text(size = 10, colour = "black"),
      axis.text.y = element_text(size = 10, colour = "black"),
      axis.title.x = element_text(face = "bold", size = 13, margin = margin(t = 6)),
      axis.title.y = element_text(face = "bold", size = 13, margin = margin(r = 10)),
      legend.position = "top",
      legend.title = element_blank(),
      legend.text = element_text(size = 12),
      legend.key.width = unit(1.2, "lines"),
      plot.margin = margin(5, right_margin, 5, left_margin)
    )
  
  # Add OR (95% CI) labels if requested
  if (add_orci_labels) {
    p <- p +
      geom_text(
        data = df_plot,
        aes(x = x_text, y = y_lab, label = label, colour = source),
        hjust = 0,
        size = label_size,
        family = "Arial",
        show.legend = FALSE
      ) +
      annotate(
        "text",
        x = x_text,
        y = min(df_plot$y_num, na.rm = TRUE) - 0.65,
        label = "OR (95% CI)",
        hjust = 0,
        vjust = 1,
        size = label_size + 0.3,
        family = "Arial",
        fontface = "bold",
        colour = "black"
      )
  }
  
  return(p)
}


