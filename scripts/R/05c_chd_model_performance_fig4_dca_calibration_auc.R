safe_logit <- function(p, eps = 1e-6) {
  qlogis(pmin(pmax(p, eps), 1 - eps))
}

fit_recal <- function(y, p, eps = 1e-6) {
  p2 <- pmin(pmax(p, eps), 1 - eps)
  fit <- glm(y ~ safe_logit(p2, eps = eps), family = binomial())
  list(
    fit = fit,
    predict = function(newp) {
      newp2 <- pmin(pmax(newp, eps), 1 - eps)
      plogis(coef(fit)[1] + coef(fit)[2] * safe_logit(newp2, eps = eps))
    }
  )
}

