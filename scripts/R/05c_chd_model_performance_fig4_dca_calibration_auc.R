# Calibration metrics
safe_logit <- function(p, eps = 1e-6) qlogis(pmin(pmax(p, eps), 1 - eps))

cal_metrics <- function(y, p, eps = 1e-6) {
  p2 <- pmin(pmax(p, eps), 1 - eps)
  
  n <- length(y)
  o <- sum(y == 1, na.rm = TRUE)
  obs_rate <- o / n
  exp_events <- sum(p2, na.rm = TRUE)
  eo <- exp_events / o
  mean_pred <- mean(p2, na.rm = TRUE)
  
  fit <- glm(y ~ safe_logit(p2, eps = eps), family = binomial())
  
  intercept <- unname(coef(fit)[1])
  slope     <- unname(coef(fit)[2])
  brier <- mean((y - p2)^2, na.rm = TRUE)
  
  tibble(
    n = n,
    events = o,
    obs_10y = obs_rate,
    mean_pred = mean_pred,
    exp_events = exp_events,
    eo_ratio = eo,
    cal_intercept = intercept,
    cal_slope = slope,
    brier = brier
  )
}

