get_nb_at <- function(tbl, model_label, pt) {
  x <- tbl %>%
    filter(label == model_label) %>%
    arrange(threshold)
  
  if (nrow(x) < 2) return(NA_real_)
  approx(x = x$threshold, y = x$net_benefit, xout = pt, rule = 2)$y
}

