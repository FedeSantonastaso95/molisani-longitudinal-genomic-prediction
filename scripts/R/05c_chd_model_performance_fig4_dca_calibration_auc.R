# Parse strings like "0.712 (0.689–0.735)"
parse_ci_vec <- function(x) {
  x <- trimws(as.character(x))
  est  <- as.numeric(str_extract(x, "^[0-9.]+"))
  low  <- as.numeric(str_extract(x, "(?<=\\()[0-9.]+"))
  high <- as.numeric(str_extract(x, "(?<=–)[0-9.]+(?=\\))"))
  if (all(is.na(high))) {
    high <- as.numeric(str_extract(x, "(?<=-)[0-9.]+(?=\\))"))
  }
  data.frame(est = est, low = low, high = high)
}

