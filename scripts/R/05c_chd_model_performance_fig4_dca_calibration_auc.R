wilson_ci <- function(x, n, z = 1.96) {
  x <- suppressWarnings(as.numeric(x))
  n <- suppressWarnings(as.numeric(n))
  
  if (is.na(x) || is.na(n) || n <= 0) return(c(lower = NA_real_, upper = NA_real_))
  x <- max(0, min(x, n))
  
  p <- x / n
  denom  <- 1 + (z^2) / n
  center <- (p + (z^2) / (2 * n)) / denom
  half   <- (z * sqrt((p * (1 - p) + (z^2) / (4 * n)) / n)) / denom
  
  lower <- max(0, center - half)
  upper <- min(1, center + half)
  
  c(lower = lower, upper = upper)
}

cal_table <- function(df_long, n_bins = 10, min_n = 50) {
  df0 <- df_long %>%
    filter(!is.na(pred), !is.na(y_t0), pred >= 0, pred <= 1) %>%
    mutate(model = droplevels(model))
  
  counts <- df0 %>% count(model, name = "n_model")
  
  bad_models <- counts %>% filter(n_model < min_n)
  if (nrow(bad_models) > 0) {
    message(
      "Dropping models with < ", min_n, " valid rows: ",
      paste(as.character(bad_models$model), collapse = ", ")
    )
  }
  
  df0 <- df0 %>%
    inner_join(counts %>% filter(n_model >= min_n), by = "model")
  
  if (nrow(df0) == 0) {
    stop("No valid data left after filtering. Check predictions / NA patterns.")
  }
  
  df0 <- df0 %>%
    group_by(model) %>%
    mutate(bin = cut_number(pred, n = n_bins, labels = FALSE)) %>%
    ungroup()
  
  out <- df0 %>%
    group_by(model, bin) %>%
    summarise(
      n = as.numeric(dplyr::n()),
      mean_pred = mean(pred),
      events = as.numeric(sum(y_t0, na.rm = TRUE)),
      obs = mean(y_t0, na.rm = TRUE),
      .groups = "drop"
    )
  
  ci_mat <- mapply(function(e, nn) wilson_ci(e, nn), out$events, out$n)
  out$obs_lwr <- as.numeric(ci_mat[1, ])
  out$obs_upr <- as.numeric(ci_mat[2, ])
  
  out
}

